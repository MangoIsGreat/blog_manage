<template>
  <div class="subject-container">
    <el-card class="head-card">
      <el-form :inline="true" :model="formInline" class="demo-form-inline">
        <el-form-item label="学科编号">
          <el-input class="short-input" v-model="formInline.rid"></el-input>
        </el-form-item>
        <el-form-item label="学科名称">
          <el-input v-model="formInline.name"></el-input>
        </el-form-item>
        <el-form-item label="创建者">
          <el-input
            class="short-input"
            v-model="formInline.username"
          ></el-input>
        </el-form-item>
        <el-form-item label="状态">
          <el-select v-model="formInline.status">
            <el-option label="禁用" value="0"></el-option>
            <el-option label="启用" value="1"></el-option>
          </el-select>
        </el-form-item>
        <el-form-item class="btn-form-item">
          <el-button type="primary" @click="getSubList">搜索</el-button>
          <el-button @click="clear">清除</el-button>
          <el-button
            @click="addFormVisible = true"
            type="primary"
            icon="el-icon-plus"
            >添加学科</el-button
          >
        </el-form-item>
      </el-form>
    </el-card>
    <el-card class="body-card">
      <el-table :data="tableData" style="width: 100%">
        <el-table-column type="index" label="序号"> </el-table-column>
        <el-table-column prop="rid" label="学科编号"> </el-table-column>
        <el-table-column prop="name" label="学科名称"> </el-table-column>
        <el-table-column prop="short_name" label="简称"> </el-table-column>
        <el-table-column prop="username" label="创建者"> </el-table-column>
        <el-table-column prop="create_time" label="创建日期"> </el-table-column>
        <el-table-column prop="status" label="状态">
          <template slot-scope="scope">
            <span v-if="scope.row.status === 1">启用</span>
            <span style="color: red" v-else>禁用</span>
          </template>
        </el-table-column>
        <el-table-column label="操作">
          <template slot-scope="scope">
            <el-button type="text">编辑</el-button>
            <el-button @click="changeStatus(scope.row)" type="text">{{
              scope.row.status === 1 ? "禁用" : "启用"
            }}</el-button>
            <el-button @click="removeItem" type="text">删除</el-button>
          </template>
        </el-table-column>
      </el-table>
      <el-pagination
        background
        @size-change="handleSizeChange"
        @current-change="handleCurrentChange"
        :current-page="page"
        :page-sizes="pageSizes"
        :page-size="limit"
        layout="total, sizes, prev, pager, next, jumper"
        :total="total"
      >
      </el-pagination>
    </el-card>
    <!-- 添加学科对话框 -->
    <addDialog />
    <!-- 编辑学科对话框 -->
    <editDialog ref="editDialog" />
  </div>
</template>

<script>
import addDialog from "./components/addDialog.vue";
import editDialog from "./components/editDialog.vue";
import {
  subjectList,
  subjectRemove,
  subjectStatus,
} from "../../../api/subject";
export default {
  data() {
    return {
      formInline: {
        rid: "",
        status: "",
        name: "",
        username: "",
      },
      tableData: [
        {
          date: "2016-05-02",
          name: "王小虎",
          address: "上海市普陀区金沙江路 1518 弄",
        },
        {
          date: "2016-05-04",
          name: "王小虎",
          address: "上海市普陀区金沙江路 1517 弄",
        },
        {
          date: "2016-05-01",
          name: "王小虎",
          address: "上海市普陀区金沙江路 1519 弄",
        },
        {
          date: "2016-05-03",
          name: "王小虎",
          address: "上海市普陀区金沙江路 1516 弄",
        },
      ],
      addFormVisible: false,
      editFormVisible: false,
      page: 1, // 页码
      limit: 2, // 每页数据条数
      pageSizes: [2, 4, 6, 9], // 页容量选项
      total: 0, // 总条数
    };
  },
  components: {
    addDialog,
    editDialog,
  },
  created() {
    // 获取列表数据
    this.getSubList();
  },
  methods: {
    showEdit(item) {
      // 显示对话框
      this.editFormVisible = true;

      this.$refs.editDialog.editForm = JSON.parse(JSON.stringify(item));
    },
    changeStatus(item) {
      subjectStatus({
        id: item.id,
      }).then((res) => {
        if (res.code === 200) {
          this.$message.success("状态修改成功！");

          this.getSubList();
        }
      });
    },
    removeItem(item) {
      this.$confirm(`你真的要删除 ${item.intro}`, "友情提示", {
        confirmButtonText: "确定",
        cancelButtonText: "取消",
        type: "warning",
      })
        .then(() => {
          subjectRemove({
            id: item.id,
          }).then((res) => {
            if (res.code === 200) {
              this.$message.success("删除成功！");
            }
          });
        })
        .catch(() => {});
    },
    // 页码改变
    handleSizeChange(page) {
      this.page = page;

      this.getSubList();
    },
    // 页容量改变
    handleCurrentChange(size) {
      // 保存页容量
      this.limit = size;

      this.getSubList();
    },
    clear() {
      for (const key in this.formInline) {
        if (Object.hasOwnProperty.call(this.formInline, key)) {
          this.formInline[key] = "";
        }
      }

      this.getSubList();
    },
    getSubList() {
      subjectList({
        page: this.page,
        limit: this.limit,
        ...this.formInline,
      }).then((res) => {
        // 保存表格数据
        this.tableData = res.data.items;

        // 保存总条数
        this.total = res.data.pagination.total;
      });
    },
  },
};
</script>

